package models;

public class CodigoQr {
	public String url;
	public String ambiente;
	public String codGeneracion;
	public String fechaEmi;
	
	public CodigoQr() {
		
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getAmbiente() {
		return ambiente;
	}

	public void setAmbiente(String ambiente) {
		this.ambiente = ambiente;
	}

	public String getCodGeneracion() {
		return codGeneracion;
	}

	public void setCodGeneracion(String codGeneracion) {
		this.codGeneracion = codGeneracion;
	}

	public String getFechaEmi() {
		return fechaEmi;
	}

	public void setFechaEmi(String fechaEmi) {
		this.fechaEmi = fechaEmi;
	}
	
	
	
	
	
}
